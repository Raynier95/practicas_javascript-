// En JS hay varios tipos de operadores entre los cuales tenemos:
// Matematicos, Asignacion e Incremento/Decremento

// Matematicos
// suma / concatenar -> +
// resta -> - 
// multiplicacion -> *
// division -> /
//  modulo -> % 

// Asignacion
// Asignacion -> =
//  suma -> a+=3 -> (a = a + 3)
//  resta -> a-=3 -> (a = a - 3)
// mult -> a*=3 -> (a = a * 3)
// div -> a/=3 -> (a = a / 3)
// mod -> a%=3 -> (a = a % 3)

// Incremento / Decremento
// post-incremento en 1 = a++
// pre-incremento en 1 = ++a
// post-decremento en 1 = a--
// pre-decremento en 1 = --a